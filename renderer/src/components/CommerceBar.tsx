export function CommerceBar({ phone, whatsappNumber, socialLinks = [], ctaButton, currency = "RON", contentMaxWidth = "max-w-7xl" }: CommerceBarProps) {
    const { getUrl } = useTenantUrl();

    return (
        <div className="hidden md:block bg-muted/50 border-b text-sm">
            <div className={`mx-auto ${contentMaxWidth} px-4 sm:px-6 lg:px-8`}>
                <div className="flex h-10 items-center justify-between">
                    {/* Left: Phone + WhatsApp */}
                    <div className="flex items-center gap-4">
                        {phone && (
                            <a
                                href={`tel:${phone.replace(/\s/g, "")}`}
                                className="flex items-center gap-1.5 text-muted-foreground hover:text-foreground transition-colors"
                            >
                                <Phone className="h-3.5 w-3.5" />
                                <span>{phone}</span>
                            </a>
                        )}
                        {whatsappNumber && (
                            <a
                                href={`https://wa.me/${whatsappNumber.replace(/[^0-9]/g, "")}`}
                                target="_blank"
                                rel="noopener noreferrer"
                                className="text-muted-foreground hover:text-foreground transition-colors"
                                aria-label="WhatsApp"
                            >
                                <WhatsAppIcon className="h-4 w-4" />
                            </a>
                        )}
                    </div>

                    {/* Right: Social + Cart + CTA */}
                    <div className="flex items-center gap-3">
                        {/* Social icons */}
                        {socialLinks.map((link, i) => (
                            <a
                                key={i}
                                href={link.url}
                                target="_blank"
                                rel="noopener noreferrer"
                                className="text-muted-foreground hover:text-foreground transition-colors"
                                aria-label={link.platform}
                            >
                                <SocialIcon platform={link.platform} />
                            </a>
                        ))}

                        {/* Cart with total */}
                        <CartWidget showTotal currency={currency} />

                        {/* CTA Button */}
                        {ctaButton && (
                            <Link
                                href={getUrl(ctaButton.url)}
                                className="bg-primary text-primary-foreground px-5 py-1.5 rounded-full text-sm font-semibold hover:opacity-90 transition-opacity whitespace-nowrap"
                            >
                                {ctaButton.text}
                            </Link>
                        )}
                    </div>
                </div>
            </div>
        </div>
    );
}
